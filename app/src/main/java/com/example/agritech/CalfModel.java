package com.example.agritech;


public class CalfModel {

    private int calf;
    private String name;
    private String born;
    private String sex;
//getter and setter methods for all of the classes
    public int getCalf() {
        return calf;
    }


    public void setCalf(int calf) {
        this.calf = calf;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name= name;
    }

    public String getBorn() {
        return born;
    }

    public void setBorn(String born  ) {
        this.born= born;
    }

    public String getSex() {
        return sex;
    }


    public void setSex(String sex) {
        this.sex= sex;
    }


}

package com.example.agritech;

public class TennisModel {

    private String name;
    private String sex;
    private String age;
    private String milking_status;
    private String farm_since;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }


    public String getMilking_status() {
        return milking_status;
    }

    public void setMilking_status(String milking_status) {
        this.milking_status = milking_status;
    }

    public String getFarm_since() {
        return farm_since;
    }

    public void setFarm_since(String farm_since) {
        this.farm_since = farm_since;
    }
}

